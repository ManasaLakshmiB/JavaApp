package com.example.javaapp.model;

public class Flags {

    private String png;
    private String alt;

    public Flags(String svg, String alt) {
        this.png = png;
        this.alt = alt;
    }

    public String getPng() {
        return png;
    }

    public void setPng(String svg) {
        this.png = png;
    }

    public String getAlt() {
        return alt;
    }

    public void setAlt(String alt) {
        this.alt = alt;
    }
}
